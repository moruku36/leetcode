# Solutionというクラスを定義し、引数として整数xを受け取り、その桁を逆にした整数を返すメソッドreverseを作る
class Solution:
    def reverse(self, x: int) -> int:
        # 逆にした数字を格納する変数revを初期化し、0にする
        rev = 0
        # xの符号をチェックし、負の場合は-1、正の場合は1とする変数signを作る
        sign = -1 if x < 0 else 1
        # xを正の数にするためにabs関数を使う
        x = abs(x)
        # xが0になるまで繰り返すwhileループを使う
        while x > 0:
            # xの最後の桁を10で割った余り（x % 10）を求めてrevに加える前にrevを10倍する。これでrevはxの桁の逆順になる
            rev = rev * 10 + x % 10
            # xの最後の桁を取り除くために10で割った商（//）を求める。これでxは小さくなっていく
            x //= 10
        # ループが終わったら、revが32ビット符号付き整数の範囲[-2^31, 2^31 - 1]内にあるかどうかif文でチェックする。もし範囲内なら、revにsignを掛けて元の符号に戻して返す。もし範囲外なら、問題文に従って0を返す

        if -2**31 <= rev <= 2**31 - 1:
            # revにsignを掛けて元の符号に戻して返す
            return sign * rev
        else:
            # 範囲外なら0を返す
            return 0